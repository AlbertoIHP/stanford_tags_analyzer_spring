package back.meet;

public class AnalizedPdfResponse {
	private String[] abbreviation_tags;
	private String[] description_tags;
	private int[] values_tags;
	
	
	
	public String[] getAbbreviation_tags() {
		return abbreviation_tags;
	}
	public void setAbbreviation_tags(String[] abbreviation_tags) {
		this.abbreviation_tags = abbreviation_tags;
	}
	public String[] getDescription_tags() {
		return description_tags;
	}
	public void setDescription_tags(String[] description_tags) {
		this.description_tags = description_tags;
	}
	public int[] getValues_tags() {
		return values_tags;
	}
	public void setValues_tags(int[] values_tags) {
		this.values_tags = values_tags;
	}
	
	
	

}
